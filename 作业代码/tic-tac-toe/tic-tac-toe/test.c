#define _CRT_SECURE_NO_WARNINGS 1
#include"game.h"
void game()
{
	char board[ROW][COL] = { 0 };
	char ret = 0;
	InitBoard(board, ROW, COL);//初始化棋盘
	DisplayBoard(board, ROW, COL);//打印棋盘
	while (1)
	{
		PlayerMove(board, ROW, COL);//玩家走
		DisplayBoard(board, ROW, COL);
		ret = cheakWin(board, ROW, COL);//判断输赢
		if (ret != 'C')
			break;
		
		ComputerMove(board, ROW, COL);//电脑走
		DisplayBoard(board, ROW, COL);
		ret = cheakWin(board, ROW, COL);//判断输赢
		if (ret != 'C')
			break;
	
	}
	if (ret == '*')
	{
		printf("玩家赢！\n");
	}
	if (ret == '#')
	{
		printf("电脑赢！\n");
	}
	if (ret == 'Q')
	{
		printf("平局！\n");
	}
}
void menu()
{
	printf("*******************\n");
	printf("***    1.play   ***\n");
	printf("***    0.exit   ***\n");
	printf("*******************\n");

}
void test()
{
	int input = 0;
	srand((unsigned)time(NULL));
	do
	{
		menu();
		printf("请选择");
		scanf("%d", &input);
		switch (input)
		{
			case 0:
				printf("退出！\n");
				break;
			case 1:
				game();
				break;
			default:
				printf("输入错误，请重新输入\n");
				break;
		}
	} while (input);
}
int main()
{
	test();
	system("pause");
	return 0;
}