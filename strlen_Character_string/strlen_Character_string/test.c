//递归求字符串长度
int my_strlen(char *str)
{
	if (*str != '\0')
		return 1+ my_strlen(str + 1);
	else
		return 0;
}
int main()
{
	int len = my_strlen("aeqweq");
	printf("%d\n", len);
	system("pause");
	return 0;
}